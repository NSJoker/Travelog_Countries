import Foundation
import SwiftUI
import UIKit

extension UIColor {
    static let shadow = UIColor(named: "Shadow") ?? UIColor.black
    static let BGColor = UIColor(named: "BGColor") ?? UIColor.white
    static let searchBG = UIColor(named: "SearchBG") ?? UIColor.black
    static let infoShadow = UIColor(named: "InfoShadow") ?? UIColor.black
    static let gold = UIColor(named: "Gold") ?? UIColor.yellow
    static let onboarding = UIColor(named: "Onboarding") ?? .purple
}

extension Color {
    static let onboarding = Color(.onboarding)
}
